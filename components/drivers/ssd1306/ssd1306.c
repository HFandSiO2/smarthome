// Send command
static void send_cmd(ssd1306_dev_t *dev, uint8_t cmd) {
    spi_transaction_t t;
    memset(&t, 0, sizeof(t));
    t.length = 8;
    t.tx_buffer = &cmd;
    t.user = (void*)0; // DC=0 = command
    gpio_set_level(dev->dc_gpio, 0);
    spi_device_polling_transmit(dev->spi, &t);
}

// Send data
static void send_data(ssd1306_dev_t *dev, const uint8_t *data, size_t len) {
    spi_transaction_t t;
    memset(&t, 0, sizeof(t));
    t.length = len * 8;
    t.tx_buffer = data;
    t.user = (void*)1; // DC=1 = data
    gpio_set_level(dev->dc_gpio, 1);
    spi_device_polling_transmit(dev->spi, &t);
}

static ssd1306_handle_t ssd1306_create(int cs_gpio, int dc_gpio, int rst_gpio) {

    ssd1306_dev_t *dev = calloc(1, sizeof(ssd1306_dev_t));
    if (!dev) return NULL;

    dev->dc_gpio = dc_gpio;

    // Configure DC pin
    gpio_config_t io_conf = {
        .pin_bit_mask = (1ULL << dc_gpio) | (1ULL << rst_gpio),
        .mode = GPIO_MODE_OUTPUT,
        .pull_up_en = GPIO_PULLUP_DISABLE,
        .pull_down_en = GPIO_PULLDOWN_DISABLE,
        .intr_type = GPIO_INTR_DISABLE,
    };
    gpio_config(&io_conf);

    // Reset
    gpio_set_level(rst_gpio, 0);
    vTaskDelay(pdMS_TO_TICKS(10));
    gpio_set_level(rst_gpio, 1);
    vTaskDelay(pdMS_TO_TICKS(10));

    // SPI device
    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = 8000000,
        .mode = 0,
        .spics_io_num = cs_gpio,
        .queue_size = 1,
    };
    ESP_ERROR_CHECK(spi_bus_add_device(SPI2_HOST, &devcfg, &dev->spi));

    // Init sequence
    send_cmd(dev, 0xAE); // display off
    send_cmd(dev, 0x20); send_cmd(dev, 0x00); // horizontal addr mode
    send_cmd(dev, 0xA8); send_cmd(dev, 0x3F); // mux ratio
    send_cmd(dev, 0xD3); send_cmd(dev, 0x00); // display offset
    send_cmd(dev, 0x40); // start line 0
    send_cmd(dev, 0xA1); // seg remap (0xA0=normal, 0xA1=mirror)
    send_cmd(dev, 0xC8); // com scan (0xC0=normal, 0xC8=reverse)
    send_cmd(dev, 0xDA); send_cmd(dev, 0x12); // com pins
    send_cmd(dev, 0x81); send_cmd(dev, 0xCF); // contrast
    send_cmd(dev, 0xD5); send_cmd(dev, 0x80); // clock
    send_cmd(dev, 0xD9); send_cmd(dev, 0xF1); // precharge
    send_cmd(dev, 0xDB); send_cmd(dev, 0x40); // vcomh
    send_cmd(dev, 0xA4); // global display on
    send_cmd(dev, 0xA6); // normal display
    send_cmd(dev, 0x8D); send_cmd(dev, 0x14); // charge pump
    send_cmd(dev, 0xAF); // display on

    memset(dev->fb, 0, sizeof(dev->fb));
    return (ssd1306_handle_t)dev;
}

void oled_init(void) {
    ssd1306_init(PIN_CS, PIN_DC, PIN_RES);
    ssd1306_clear();
    ssd1306_refresh();
}

esp_err_t ssd1306_init(int cs_gpio, int dc_gpio, int rst_gpio) {
    s_dev = (ssd1306_dev_t *)ssd1306_create(cs_gpio, dc_gpio, rst_gpio);
    if (!s_dev) {
        ESP_LOGE(TAG, "Failed to create SSD1306");
        return ESP_FAIL;
    }
    return ESP_OK;
}

void ssd1306_clear(void) {
    memset(s_dev->fb, 0, sizeof(s_dev->fb));
}

void ssd1306_draw_pixel(int x, int y, bool on) {
    if (x < 0 || x >= SSD1306_WIDTH || y < 0 || y >= SSD1306_HEIGHT) return;

    int page = y / 8;
    int bit = y % 8;
    if (on)
        s_dev->fb[page * SSD1306_WIDTH + x] |= (1 << bit);
    else
        s_dev->fb[page * SSD1306_WIDTH + x] &= ~(1 << bit);
}

void ssd1306_draw_string(int x, int y, const char *str) {
    while (*str) {
        if (*str < 32 || *str > 126) { str++; continue; }
        int idx = *str - 32;
        for (int col = 0; col < 5; col++) {
            uint8_t data = font5x7[idx][col];
            for (int row = 0; row < 7; row++) {
                if (data & (1 << row)) {
                    ssd1306_draw_pixel(x + col, y + row, true);
                }
            }
        }
        x += 6; // 5 + 1 space
        str++;
    }
}

void ssd1306_refresh(void) {
    ssd1306_dev_t *dev = s_dev;
    // Set column address range
    send_cmd(dev, 0x21); send_cmd(dev, 0); send_cmd(dev, 127);
    // Set page address range
    send_cmd(dev, 0x22); send_cmd(dev, 0); send_cmd(dev, 7);
    // Send framebuffer
    send_data(dev, dev->fb, sizeof(dev->fb));
}